/**** SQl query to select latest 10 products from the database table ****/
SELECT TOP 10 [id]
    ,[is_new]
    ,[name]
    ,[desc]
    ,[SKU]
    ,[category_id]
    ,[inventory_id]
    ,[o_price]
    ,[d_price]
    ,[discount_id]
    ,[created_at]
    ,[modified_at]
    ,[deleted_at]
    ,[image_path]
    ,[stars]
FROM [dbo].[products]
ORDER BY [id] DESC